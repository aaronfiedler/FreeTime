from selenium import webdriver
import pause
from datetime import datetime



enter_confirmation_number = '123456'
enter_passenger_first_name = 'Aaron'
enter_passenger_last_name = 'Fiedler'
check_in_flight_year = 2024
check_in_flight_month = 6
check_in_flight_day = 19
check_in_flight_24_hour = 12
check_in_flight_min = 6



driver = webdriver.Chrome()
driver.get('https://www.southwest.com/air/check-in/index.html')

confirmation_number = driver.find_element('id', 'confirmationNumber')
passenger_first_name = driver.find_element('id', 'passengerFirstName')
passenger_last_name = driver.find_element('id', 'passengerLastName')

confirmation_number.send_keys(enter_confirmation_number)
passenger_first_name.send_keys(enter_passenger_first_name)
passenger_last_name.send_keys(enter_passenger_last_name)

checkin = driver.find_element('id', 'form-mixin--submit-button')
pause.until(datetime(check_in_flight_year, check_in_flight_month, check_in_flight_day, check_in_flight_24_hour, check_in_flight_min))
checkin.click()
